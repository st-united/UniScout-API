  async update(
    id: number,
    updateUserDto: UpdateUserDto,
    actorId: number,
    ipAddress?: string,
    userAgent?: string
  ): Promise<ResponseItem<UserDto>> {
    const user = await this.userRepository.findOneBy({ id, deletedAt: null } as FindOptionsWhere<UserEntity>);
    if (!user) {
      throw new BadRequestException('Người dùng không tồn tại');
    }
    const oldValue = { ...user, password: '[REDACTED]' };
    const updatePayload: Partial<UserEntity> = {};
    if (updateUserDto.name !== undefined) {
      updatePayload.name = updateUserDto.name;
    }

    if (updateUserDto.email !== undefined) {
      if (updateUserDto.email !== user.email) {
        const emailExisted = await this.userRepository.findOneBy({
          email: updateUserDto.email,
          id: Not(id),
          deletedAt: null,
        } as FindOptionsWhere<UserEntity>);
        if (emailExisted) {
          throw new BadRequestException('Email đã tồn tại');
        }
      }
      updatePayload.email = updateUserDto.email;
    }

    if (updateUserDto.job !== undefined) {
      updatePayload.job = updateUserDto.job;
    }

    if (updateUserDto.status !== undefined) {
      updatePayload.status = updateUserDto.status;
    }

    await this.userRepository.update(id, updatePayload);

    const result = await this.userRepository.findOneBy({ id, deletedAt: null } as FindOptionsWhere<UserEntity>);
    const newValue = { ...result, password: '[REDACTED]' };
    await this.auditLogService.log(actorId, AuditActionType.USER_UPDATED, id, oldValue, newValue, ipAddress, userAgent);
    const resultDto = plainToClass(UserDto, result, { excludeExtraneousValues: true });

    return new ResponseItem(resultDto, 'Cập nhật dữ liệu thành công');
  }

  async deleteUser(id: number): Promise<ResponseItem<null>> {
    const user = await this.userRepository.findOneBy({ id, deletedAt: null } as FindOptionsWhere<UserEntity>);
    if (!user) throw new BadRequestException('Người dùng không tồn tại');
    if (user.status === StatusEnum.ACTIVE) throw new BadRequestException('Không được xóa nhân viên đang hoạt động');

    await this.userRepository.softDelete(id);

    return new ResponseItem(null, 'Xóa nhân viên thành công');
  }

  async uploadAvatar(identityId: number, file: Express.Multer.File): Promise<ResponseItem<any>> {
    const user = await this.userRepository.findOneBy({ identityId, deletedAt: null } as FindOptionsWhere<UserEntity>);

    if (!user) {
      throw new BadRequestException('Nhân viên không tồn tại');
    }

    await this.userRepository
      .createQueryBuilder()
      .update(UserEntity)
      .set({
        avatar: avtPathName('users', file.filename),
      })
      .where('identityId = :identityId', { identityId })
      .execute();

    if (user.avatar && fs.existsSync(user.avatar)) {
      fs.unlinkSync(user.avatar);
    }

    return new ResponseItem(null, 'Cập nhật thông tin thành công');
  }

  async removeAvatar(identityId: number): Promise<ResponseItem<any>> {
    const user = await this.userRepository.findOneBy({ identityId, deletedAt: null } as FindOptionsWhere<UserEntity>);

    if (!user) {
      throw new BadRequestException('Nhân viên không tồn tại');
    }

    await this.userRepository
      .createQueryBuilder()
      .update(UserEntity)
      .set({
        avatar: null,
      })
      .where('identityId = :identityId', { identityId })
      .execute();

    if (user.avatar && fs.existsSync(user.avatar)) {
      fs.unlinkSync(user.avatar);
    }

    return new ResponseItem(null, 'Xóa ảnh đại diện thành công');
  }
